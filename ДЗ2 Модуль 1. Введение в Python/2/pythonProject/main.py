# Задание 2
# Пользователь вводит с клавиатуры число, состоящее
# из четырех цифр. Требуется найти произведение цифр.
# Например, если с клавиатуры введено 1324, тогда результат произведения 1*3*2*4 = 24.

# Получаем ввод от пользователя
number_str = input("Введите четырёхзначное число: ")

# Проверяем, что длина введённой строки равна 4
if len(number_str) != 4:
    print("Ошибка! Введенное значение должно содержать ровно четыре цифры.")
else:
    # Переводим каждую цифру в целое число и перемножаем их
    product = 1
    for char in number_str:
        product *= int(char)

    # Выводим результат
    print(f"Произведение цифр числа {number_str}: {product}")

