# Задание 1:
# Пользователь вводит с клавиатуры арифметическое
# выражение. Например, 23+12.
# Необходимо вывести на экран результат выражения.
# В нашем примере это 35. Арифметическое выражение
# может состоять только из трёх частей: число, операция,
# число. Возможные операции: +, -,*,/

# Получаем ввод от пользователя
expression = input('Введите арифметическое выражение: ')

# Разделяем строку на части
parts = expression.split()

if len(parts) != 3:
    print('Неверный формат ввода! Должны быть три элемента.')
else:
    try:
        # Преобразуем первые и последние элементы в числа
        num1 = float(parts[0])
        num2 = float(parts[2])

        # Определяем операцию
        operation = parts[1]

        if operation == '+':
            result = num1 + num2
        elif operation == '-':
            result = num1 - num2
        elif operation == '*':
            result = num1 * num2
        elif operation == '/':
            if num2 == 0:
                print('Ошибка! Деление на ноль!')
            else:
                result = num1 / num2
        else:
            print('Неизвестная операция!')

        # Выводим результат
        print('Результат:', result)

    except ValueError:
        print('Ошибка! Неверный формат чисел!')
