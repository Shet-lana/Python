# Задание 2:
# В списке целых, заполненном случайными числами,
# определить минимальный и максимальный элементы,
# посчитать количество отрицательных элементов, посчитать количество
# положительных элементов, посчитать
# количество нулей. Результаты вывести на экран.

import random  # Импортируем модуль для генерации случайных чисел

var_list = []  # Создаём пустой список для хранения случайных чисел
for i in range(10):  # Цикл для добавления 10 случайных чисел в список
    var_list.append(random.randint(-50, 50))  # Генерируем случайное целое число от -50 до 50 и добавляем его в список

print(var_list)  # Выводим содержимое списка на экран

result_sum = 0  # Переменная для хранения суммы всех элементов списка
for i in var_list:  # Проходим по всем элементам списка
    result_sum += i  # Прибавляем текущее число к сумме
print('сумма всех элементов списка', result_sum)  # Выводим сумму всех элементов списка

max_num = var_list[0]  # Присваиваем переменной max_num первый элемент списка
for i in var_list:  # Проходим по всем элементам списка
    if i > max_num:  # Если текущий элемент больше текущего максимального
        max_num = i  # То обновляем значение максимальной переменной
print('максимальное число из списка', max_num)  # Выводим максимальное число из списка

min_num = var_list[0]  # Присваиваем переменной min_num первый элемент списка
for i in var_list:  # Проходим по всем элементам списка
    if i < min_num:  # Если текущий элемент меньше текущего минимального
        min_num = i  # То обновляем значение минимальной переменной
print('минимальное число из списка', min_num)  # Выводим минимальное число из списка

negative_count = 0  # Переменная для подсчета отрицательных чисел
for i in var_list:  # Проходим по всем элементам списка
    if i < 0:  # Если текущий элемент отрицательный
        negative_count += 1  # Увеличиваем счетчик отрицательных чисел
print('количество отрицательных чисел', negative_count)  # Выводим количество отрицательных чисел

positive_count = 0  # Переменная для подсчета положительных чисел
for i in var_list:  # Проходим по всем элементам списка
    if i > 0:  # Если текущий элемент положительный
        positive_count += 1  # Увеличиваем счетчик положительных чисел
print('количество положительных чисел', positive_count)  # Выводим количество положительных чисел

zero_count = 0  # Переменная для подсчета нулей
for i in var_list:  # Проходим по всем элементам списка
    if i == 0:  # Если текущий элемент равен нулю
        zero_count += 1  # Увеличиваем счетчик нулей
print('количество нулей', zero_count)  # Выводим количество нулей
