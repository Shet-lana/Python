# Задание 2
# Написать программу, которая по выбору пользователя возводит введенное им число в степень от нулевой
# до седьмой включительно.

def power_of_number():
    # Получаем число от пользователя
    base = float(input("Введите число: "))

    # Выводим варианты степеней
    print("Выберите степень:")
    for i in range(8):
        print(f"{i}. Возвести в {i}-ую степень")

    # Получаем выбор степени от пользователя
    choice = int(input("Ваш выбор: "))

    # Проверяем, что выбор находится в допустимом диапазоне
    if choice >= 0 and choice <= 7:
        result = base ** choice
        print(f"Ваше число {base} возведено в степень {choice}: {result}")
    else:
        print("Неверный выбор!")


# Вызываем функцию
power_of_number()

