# Задание 3
# Написать программу подсчета стоимости разговора
# для разных мобильных операторов. Пользователь вводит
# стоимость разговора и выбирает с какого на какой оператор он звонит. Вывести стоимость на экран.

# Создаем словарь тарифов для каждого оператора
tariffs = {
    'МТС': {'МТС': 1, 'Билайн': 2, 'Мегафон': 3},
    'Билайн': {'МТС': 2, 'Билайн': 1, 'Мегафон': 3},
    'Мегафон': {'МТС': 3, 'Билайн': 3, 'Мегафон': 1}
}

# Функция для расчета стоимости разговора
def calculate_cost(duration, operator_from, operator_to):
    cost_per_minute = tariffs[operator_from][operator_to]
    total_cost = duration * cost_per_minute
    return total_cost

# Основной блок программы
if __name__ == "__main__":
    # Запрашиваем у пользователя длительность разговора и выбираем операторов
    duration = float(input("Введите продолжительность разговора в минутах: "))
    operator_from = input("Выберите оператора исходящего звонка (МТС/Билайн/Мегафон): ")
    operator_to = input("Выберите оператора принимающего звонка (МТС/Билайн/Мегафон): ")

    # Проверяем, что операторы введены правильно
    if operator_from not in tariffs or operator_to not in tariffs[operator_from]:
        print("Некорректный ввод оператора.")
    else:
        # Рассчитываем и выводим стоимость
        cost = calculate_cost(duration, operator_from, operator_to)
        print(f"Стоимость разговора: {cost:.2f} рублей.")
