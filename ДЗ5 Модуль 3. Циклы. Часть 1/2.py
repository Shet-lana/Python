# Задание 2
# Пользователь вводит с клавиатуры два числа (начало и конец диапазона). Требуется проанализировать все числа в этом диапазоне. Нужно вывести на экран:
# 1. Все числа диапазона;
# 2. Все числа диапазона в убывающем порядке;
# 3. Все числа, кратные 7;
# 4. Количество чисел, кратных 5.

# Запрашиваем у пользователя начало и конец диапазона
start = int(input("Введите начало диапазона: "))
end = int(input("Введите конец диапазона: "))

# Создаем список всех чисел в указанном диапазоне
numbers = list(range(start, end + 1))

# Выводим все числа диапазона
print("\nВсе числа диапазона:")
print(numbers)

# Выводим все числа диапазона в убывающем порядке
reversed_numbers = numbers[::-1]
print("\nВсе числа диапазона в убывающем порядке:")
print(reversed_numbers)

# Находим и выводим все числа, кратные 7
multiples_of_7 = [num for num in numbers if num % 7 == 0]
print("\nВсе числа, кратные 7:")
print(multiples_of_7)

# Считаем количество чисел, кратных 5
count_multiples_of_5 = sum(1 for num in numbers if num % 5 == 0)
print(f"\nКоличество чисел, кратных 5: {count_multiples_of_5}")
